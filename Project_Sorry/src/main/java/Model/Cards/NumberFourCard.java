package Model.Cards;

/**
 * Move a pawn four positions backward (i.e., opposite to the clockwise direction).
 * @author Nikolas Lazaridis
 * @version 1.0
 */
public class NumberFourCard extends NumberCard {
    /**
     *<b>constructor</b>:Constructs a new instance of card 4 and via the
     *parent class NumberCard sets with the command super,
     *value=4 and name=4.
     */
    public NumberFourCard() {
        super(4);
    }
}
